package org.sonatype.nexus.capability;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import javax.validation.Constraint;
import javax.validation.Payload;

import org.sonatype.nexus.capability.internal.UniquePerCapabilityTypeValidator;

import static java.lang.annotation.ElementType.ANNOTATION_TYPE;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * Validate capability type existence.
 *
 * @since 3.0
 */
@Target({TYPE, ANNOTATION_TYPE})
@Retention(RUNTIME)
@Constraint(validatedBy = UniquePerCapabilityTypeValidator.class)
public @interface UniquePerCapabilityType
{
  // TODO can we have a better message in validator saying what type from descriptor
  String message() default "Only one capability of this type can be created";

  Class<?>[] groups() default {};

  Class<? extends Payload>[] payload() default {};

  String value();
}
